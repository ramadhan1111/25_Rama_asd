public class Pakaian{
    //atribut
    String jenis;
    String warna;
    String merk;
    String ukuran;
    double harga;

    //konstruktor berparameter
    Pakaian(String jenis, String warna, String merk, String ukuran, double harga){
        this.jenis = jenis;
        this.warna = warna;
        this.merk = merk;
        this.ukuran = ukuran;
        this.harga = harga;

    }
}
